// rules for naming variables

// you can not start with number
// example :-
// 1value (invalid)
// value1 (valid)

var value1 = 10;
console.log(value1);

// you can use only undersore _ or dollar symble
// first_name (valid)
// _firstname (valid)

// first$name (valid)
// $firstname (valid)

// you cannot use spaces 
var first_name = "arjun";  // snake case writing
var firstname = "arjun";  // camel case writing
console.log(first_name , firstname);

// first name (invalid)

